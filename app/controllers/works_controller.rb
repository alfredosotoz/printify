  end

  private
    # Use callbacks to share common setup or constraints between actions.
    def set_work
      @work = Work.find(params[:id])
    end

    # Never trust parameters from the scary internet, only allow the white list through.
    def work_params
      params.require(:work).permit(:nombre, :tamano_de_papel, :tipo_de_papel, :numero_de_paginas_impresas, :fecha, :estado_de_impresion, :usuario, :numero_de_paginas_blanco_negro, :numero_de_paginas_a_color, :numero_de_copias_impresas, :paginas)
    end
end
